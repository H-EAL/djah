#include "system/input/keyboard.hpp"
#include "platform.hpp"

namespace djah { namespace system { namespace input {

	//----------------------------------------------------------------------------------------------
	keyboard::keyboard()
		: keys_state_(256)
	{
	}
	//----------------------------------------------------------------------------------------------

	//----------------------------------------------------------------------------------------------
	keyboard::~keyboard()
	{
	}
	//----------------------------------------------------------------------------------------------

	//----------------------------------------------------------------------------------------------
	void keyboard::update()
	{
		GetKeyboardState( (PBYTE)&keys_state_[0] );
	}
	//----------------------------------------------------------------------------------------------
	
	//----------------------------------------------------------------------------------------------
	bool keyboard::isKeyDown(E_KEY_CODE key_code)
	{
		return (keys_state_[key_code] & 0x80) != 0;
	}
	//----------------------------------------------------------------------------------------------

} /*input*/ } /*system*/ } /*djah*/