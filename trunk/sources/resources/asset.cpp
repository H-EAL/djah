	{

	}
	//----------------------------------------------------------------------------------------------


	//----------------------------------------------------------------------------------------------
	asset::~asset()
	{
		asset_warehouse::get().remove(name_);
	}
	//----------------------------------------------------------------------------------------------


	//----------------------------------------------------------------------------------------------
	const std::string& asset::name() const
	{
		return name_;
	}
	//----------------------------------------------------------------------------------------------


	//----------------------------------------------------------------------------------------------
	void asset::setName(const std::string &name)
	{
		name_ = name;
	}
