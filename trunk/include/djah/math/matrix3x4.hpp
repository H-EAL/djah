#ifndef DJAH_MATH_MATRIX3X4_HPP
#define DJAH_MATH_MATRIX3X4_HPP

#include "matrix.hpp"

namespace djah { namespace math {
	
	//----------------------------------------------------------------------------------------------
	template<typename T>
	struct matrix_base<3,4,T>
	{
		matrix_base() { memset(data, 0, 3*4*sizeof(T)); }
		matrix_base(T _11_, T _12_, T _13_, T _14_,  T _21_, T _22_, T _23_, T _24_,  T _31_, T _32_, T _33_, T _34_)
			: _11(_11_), _12(_12_), _13(_13_), _14(_14_)
			, _21(_21_), _22(_22_), _23(_23_), _24(_24_)
			, _31(_31_), _32(_32_), _33(_33_), _34(_34_)
		{}
		
		union
		{
			struct
			{
				T _11, _12, _13, _14;
				T _21, _22, _23, _24;
				T _31, _32, _33, _34;
			};
			T data[3][4];
		};
	};
	//----------------------------------------------------------------------------------------------

	//----------------------------------------------------------------------------------------------
	typedef matrix<3,4,int>		matrix3x4i;
	typedef matrix<3,4,float>	matrix3x4f;
	typedef matrix<3,4,double>	matrix3x4d;
	//----------------------------------------------------------------------------------------------

} /*math*/ } /*djah*/

#endif /* DJAH_MATH_MATRIX3X4_HPP */